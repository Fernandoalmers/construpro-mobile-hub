
import { useState, useCallback, useEffect, useMemo } from 'react';
import { useQuery, useMutation, useQueryClient } from '@tanstack/react-query';
import { addressService, Address } from '@/services/addressService';
import { addressCacheService } from '@/services/addressCacheService';
import { toast } from '@/components/ui/use-toast';
import { useAuth } from '@/context/AuthContext';

export function useAddresses() {
  const [isAddModalOpen, setIsAddModalOpen] = useState(false);
  const [editingAddress, setEditingAddress] = useState<Address | null>(null);
  const [errorDetails, setErrorDetails] = useState<string | null>(null);
  const [cachedAddresses, setCachedAddresses] = useState<Address[]>([]);
  const [isCacheLoaded, setIsCacheLoaded] = useState(false);
  const queryClient = useQueryClient();
  const { refreshProfile, user } = useAuth();

  // Load cache only once
  useEffect(() => {
    if (isCacheLoaded) return;
    
    console.log('[useAddresses] üè† Carregando endere√ßos do cache...');
    
    try {
      const cached = addressCacheService.loadFromCache();
      if (cached && cached.length > 0) {
        setCachedAddresses(cached);
        console.log('[useAddresses] ‚úÖ Cache carregado:', cached.length);
      }
    } catch (error) {
      console.warn('[useAddresses] ‚ö†Ô∏è Erro ao carregar cache:', error);
    } finally {
      setIsCacheLoaded(true);
    }
  }, [isCacheLoaded]);

  const formatErrorMessage = (error: any): string => {
    console.error("‚ùå [useAddresses] Error:", error);
    let errorMessage = 'Um erro inesperado ocorreu.';
    
    if (error instanceof Error) {
      errorMessage = error.message;
    } else if (typeof error === 'object' && error !== null) {
      errorMessage = error.message || JSON.stringify(error);
    } else if (typeof error === 'string') {
      errorMessage = error;
    }
    
    setErrorDetails(typeof error === 'object' ? JSON.stringify(error) : String(error));
    return errorMessage;
  };

  const validateCEP = (cep: string): boolean => {
    const cleanCep = cep.replace(/\D/g, '');
    if (cleanCep.length !== 8) return false;
    if (cleanCep === '00000000' || /^(\d)\1{7}$/.test(cleanCep)) return false;
    return true;
  };

  const validateAddress = (address: Address): { isValid: boolean; errors: string[] } => {
    const errors: string[] = [];
    
    if (!address.nome?.trim()) errors.push('Nome √© obrigat√≥rio');
    if (!address.cep?.trim()) {
      errors.push('CEP √© obrigat√≥rio');
    } else if (!validateCEP(address.cep)) {
      errors.push('CEP deve ter 8 d√≠gitos e ser v√°lido');
    }
    if (!address.logradouro?.trim()) errors.push('Logradouro √© obrigat√≥rio');
    if (!address.numero?.trim()) errors.push('N√∫mero √© obrigat√≥rio');
    if (!address.bairro?.trim()) errors.push('Bairro √© obrigat√≥rio');
    if (!address.cidade?.trim()) errors.push('Cidade √© obrigat√≥ria');
    if (!address.estado?.trim()) errors.push('Estado √© obrigat√≥rio');
    
    return { isValid: errors.length === 0, errors };
  };

  // Fetch addresses with longer cache
  const { 
    data: serverAddresses = [], 
    isLoading: isLoadingServer, 
    error, 
    refetch
  } = useQuery({
    queryKey: ['addresses'],
    queryFn: async () => {
      try {
        console.log("üîÑ [useAddresses] Sincronizando endere√ßos com servidor...");
        
        if (!user?.id) {
          throw new Error('Usu√°rio n√£o autenticado');
        }
        
        const data = await addressService.getUserAddresses(user.id);
        
        // Save to cache after success
        addressCacheService.saveToCache(data);
        setCachedAddresses(data);
        
        console.log("‚úÖ [useAddresses] Endere√ßos sincronizados:", data.length);
        setErrorDetails(null);
        return data;
      } catch (err) {
        const errorMsg = formatErrorMessage(err);
        console.error("‚ùå [useAddresses] Erro ao sincronizar:", errorMsg);
        throw err;
      }
    },
    retry: 1,
    retryDelay: 2000,
    staleTime: 2 * 60 * 1000,
    gcTime: 10 * 60 * 1000,
    refetchOnWindowFocus: false,
    refetchOnMount: false,
    enabled: isCacheLoaded && !!user?.id
  });

  // Prioritize data without reactive changes
  const addresses = useMemo(() => {
    if (serverAddresses.length > 0) {
      return serverAddresses;
    }
    return cachedAddresses;
  }, [serverAddresses, cachedAddresses]);
  
  const isLoading = !isCacheLoaded || (cachedAddresses.length === 0 && isLoadingServer);

  // Set primary address mutation with complete sync
  const setPrimaryAddressMutation = useMutation({
    mutationFn: async (addressId: string) => {
      try {
        console.log(`[useAddresses] üè† Definindo endere√ßo ${addressId} como principal`);
        
        if (!user?.id) {
          throw new Error('Usu√°rio n√£o autenticado');
        }
        
        const currentAddresses = addresses;
        const addressToSet = currentAddresses.find(addr => addr.id === addressId);
        
        if (!addressToSet) {
          throw new Error('Endere√ßo n√£o encontrado. Atualize a p√°gina e tente novamente.');
        }
        
        await addressService.setPrimaryAddress(addressId, user.id);
        
        console.log(`[useAddresses] ‚úÖ Endere√ßo principal definido`);
        return { addressId, newCep: addressToSet.cep };
      } catch (err) {
        const errorMsg = formatErrorMessage(err);
        console.error("[useAddresses] ‚ùå Erro ao definir endere√ßo principal:", errorMsg);
        throw err;
      }
    },
    onSuccess: async (result) => {
      const { addressId, newCep } = result;
      console.log(`[useAddresses] üîÑ Sincroniza√ß√£o completa para endere√ßo:`, addressId);
      
      try {
        // Force refresh profile in AuthContext
        console.log(`[useAddresses] üìã For√ßando refresh do perfil...`);
        await refreshProfile();
        
        // Wait for sync
        await new Promise(resolve => setTimeout(resolve, 800));
        
        // Invalidate and force refetch queries
        console.log(`[useAddresses] üóÇÔ∏è Invalidando cache...`);
        queryClient.invalidateQueries({ queryKey: ['addresses'] });
        addressCacheService.clearCache();
        setCachedAddresses([]);
        
        // Dispatch custom event for cross-page communication
        console.log(`[useAddresses] üì° Disparando evento de mudan√ßa...`);
        window.dispatchEvent(new CustomEvent('primary-address-changed', {
          detail: { 
            newCep, 
            addressId,
            timestamp: Date.now()
          }
        }));
        
        // Force immediate refetch
        setTimeout(() => {
          refetch();
        }, 100);
        
        console.log(`[useAddresses] üéâ Sincroniza√ß√£o completa finalizada`);
        
        toast({
          title: "‚úÖ Endere√ßo principal atualizado",
          description: "Endere√ßo definido como principal e sincronizado.",
          duration: 3000
        });
      } catch (syncError) {
        console.error(`[useAddresses] ‚ö†Ô∏è Erro na sincroniza√ß√£o:`, syncError);
        toast({
          variant: "destructive",
          title: "‚ö†Ô∏è Endere√ßo atualizado com aviso",
          description: "Endere√ßo alterado, mas pode precisar de alguns segundos para sincronizar."
        });
      }
    },
    onError: (error: any) => {
      console.error(`[useAddresses] ‚ùå Falha ao definir endere√ßo principal:`, error);
      
      const errorMsg = formatErrorMessage(error);
      toast({
        variant: "destructive",
        title: "‚ùå Erro ao definir endere√ßo principal", 
        description: errorMsg
      });
    }
  });

  // Delete address mutation
  const deleteAddressMutation = useMutation({
    mutationFn: async (addressId: string) => {
      try {
        console.log(`üóëÔ∏è [useAddresses] Removendo endere√ßo: ${addressId}`);
        return await addressService.deleteAddress(addressId);
      } catch (err) {
        const errorMsg = formatErrorMessage(err);
        console.error("‚ùå [useAddresses] Erro ao remover endere√ßo:", errorMsg);
        throw err;
      }
    },
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ['addresses'], refetchType: 'none' });
      addressCacheService.clearCache();
      toast({
        title: "‚úÖ Endere√ßo removido",
        description: "Endere√ßo removido com sucesso."
      });
    },
    onError: (error: any) => {
      const errorMsg = formatErrorMessage(error);
      toast({
        variant: "destructive",
        title: "‚ùå Erro ao remover endere√ßo",
        description: errorMsg
      });
    }
  });

  // Save address mutation with detailed logs
  const saveAddressMutation = useMutation({
    mutationFn: async (data: { address: Address, isEdit: boolean }) => {
      const timestamp = new Date().toISOString();
      
      try {
        console.log(`[${timestamp}] [saveAddressMutation] üíæ INICIANDO SALVAMENTO:`, {
          isEdit: data.isEdit,
          addressId: data.address.id,
          nome: data.address.nome,
          cep: data.address.cep,
          userId: user?.id,
          principal: data.address.principal
        });
        
        const validation = validateAddress(data.address);
        if (!validation.isValid) {
          console.error(`[${timestamp}] [saveAddressMutation] ‚ùå VALIDA√á√ÉO FALHOU:`, validation.errors);
          throw new Error(`Dados inv√°lidos: ${validation.errors.join(', ')}`);
        }
        
        const cleanedAddress = {
          ...data.address,
          cep: data.address.cep.replace(/\D/g, ''),
          user_id: user?.id || data.address.user_id
        };
        
        console.log(`[${timestamp}] [saveAddressMutation] üì§ DADOS LIMPOS:`, cleanedAddress);
        
        let result;
        if (data.isEdit && data.address.id) {
          console.log(`[${timestamp}] [saveAddressMutation] ‚úèÔ∏è EDITANDO endere√ßo existente`);
          result = await addressService.updateAddress(data.address.id, cleanedAddress);
        } else {
          console.log(`[${timestamp}] [saveAddressMutation] ‚ûï CRIANDO novo endere√ßo`);
          result = await addressService.addAddress(cleanedAddress);
        }
        
        console.log(`[${timestamp}] [saveAddressMutation] ‚úÖ RESPOSTA DO SERVIDOR:`, {
          id: result.id,
          nome: result.nome,
          cep: result.cep,
          principal: result.principal
        });
        
        return result;
      } catch (err) {
        const errorMsg = formatErrorMessage(err);
        console.error(`[${timestamp}] [saveAddressMutation] ‚ùå ERRO NO SALVAMENTO:`, {
          error: err,
          errorMessage: errorMsg
        });
        throw err;
      }
    },
    onSuccess: (result, variables) => {
      const timestamp = new Date().toISOString();
      console.log(`[${timestamp}] [saveAddressMutation] üéâ SUCESSO! Resultado:`, {
        id: result.id,
        nome: result.nome,
        isEdit: variables.isEdit
      });
      
      // Invalidate queries and clear cache
      console.log(`[${timestamp}] [saveAddressMutation] üóÇÔ∏è Invalidando queries...`);
      queryClient.invalidateQueries({ queryKey: ['addresses'] });
      addressCacheService.clearCache();
      
      // Force immediate refetch
      console.log(`[${timestamp}] [saveAddressMutation] üîÑ For√ßando refetch...`);
      setTimeout(() => {
        refetch();
      }, 100);
      
      setErrorDetails(null);
      toast({
        title: variables.isEdit ? "‚úÖ Endere√ßo atualizado" : "‚úÖ Endere√ßo adicionado",
        description: variables.isEdit 
          ? "Endere√ßo atualizado com sucesso." 
          : "Endere√ßo adicionado com sucesso."
      });
      setIsAddModalOpen(false);
      setEditingAddress(null);
      
      console.log(`[${timestamp}] [saveAddressMutation] ‚úÖ PROCESSO COMPLETO`);
    },
    onError: (error: any, variables) => {
      const timestamp = new Date().toISOString();
      console.error(`[${timestamp}] [saveAddressMutation] ‚ùå ERRO NA MUTATION:`, {
        error,
        variables,
        errorMessage: error?.message
      });
      
      const errorMsg = formatErrorMessage(error);
      toast({
        variant: "destructive",
        title: "‚ùå Erro ao salvar endere√ßo",
        description: errorMsg
      });
    }
  });

  // Enhanced retry functionality
  const retryOperation = useCallback(async () => {
    toast({
      title: "üîÑ Tentando novamente",
      description: "Tentando carregar seus endere√ßos..."
    });
    await refetch();
  }, [refetch]);

  const handleSetDefaultAddress = (addressId: string) => {
    console.log(`[useAddresses] üè† Usu√°rio solicitou definir endere√ßo ${addressId} como principal`);
    setPrimaryAddressMutation.mutate(addressId);
  };

  const handleEditAddress = (address: Address) => {
    console.log('[useAddresses] ‚úèÔ∏è Editando endere√ßo:', address);
    setEditingAddress(address);
    setIsAddModalOpen(true);
  };

  const handleDeleteAddress = (addressId: string) => {
    if (window.confirm('Tem certeza que deseja remover este endere√ßo?')) {
      deleteAddressMutation.mutate(addressId);
    }
  };

  const handleAddAddress = () => {
    console.log('[useAddresses] ‚ûï Abrindo modal para novo endere√ßo');
    setEditingAddress(null);
    setIsAddModalOpen(true);
  };

  const handleSaveAddress = (address: Address) => {
    const timestamp = new Date().toISOString();
    console.log(`[${timestamp}] [useAddresses] üíæ handleSaveAddress CHAMADO:`, {
      nome: address.nome,
      cep: address.cep,
      principal: address.principal,
      user_id: address.user_id
    });
    
    const isEdit = Boolean(editingAddress);
    console.log(`[${timestamp}] [useAddresses] üîÑ Disparando mutation, isEdit:`, isEdit);
    saveAddressMutation.mutate({ address, isEdit });
  };

  // Enhanced addAddress function for useCheckout
  const addAddress = async (formData: Partial<Address>): Promise<Address | null> => {
    const timestamp = new Date().toISOString();
    
    try {
      console.log(`[${timestamp}] [useAddresses] ‚ûï addAddress CHAMADO:`, formData);
      
      const fullAddress: Address = {
        id: '',
        user_id: user?.id || '',
        nome: formData.nome || '',
        cep: formData.cep?.replace(/\D/g, '') || '',
        logradouro: formData.logradouro || '',
        numero: formData.numero || '',
        complemento: formData.complemento || '',
        bairro: formData.bairro || '',
        cidade: formData.cidade || '',
        estado: formData.estado || '',
        principal: formData.principal || false,
        created_at: '',
        updated_at: '',
        ...formData
      };

      const validation = validateAddress(fullAddress);
      if (!validation.isValid) {
        console.error(`[${timestamp}] [useAddresses] ‚ùå VALIDA√á√ÉO FALHOU no addAddress:`, validation.errors);
        throw new Error(`Dados inv√°lidos: ${validation.errors.join(', ')}`);
      }

      console.log(`[${timestamp}] [useAddresses] üì§ Chamando addressService.addAddress...`);
      const result = await addressService.addAddress(fullAddress);
      console.log(`[${timestamp}] [useAddresses] ‚úÖ Resultado do addAddress:`, result);
      
      queryClient.invalidateQueries({ queryKey: ['addresses'] });
      addressCacheService.clearCache();
      
      return result;
    } catch (error) {
      console.error(`[${timestamp}] [useAddresses] ‚ùå Erro ao adicionar endere√ßo:`, error);
      throw error;
    }
  };

  const getPrimaryAddress = () => {
    return addresses.find(address => address.principal);
  };

  return {
    addresses,
    isLoading,
    error,
    errorDetails,
    refetch: retryOperation,
    isAddModalOpen,
    setIsAddModalOpen,
    editingAddress,
    getPrimaryAddress,
    handleSetDefaultAddress,
    handleEditAddress,
    handleDeleteAddress,
    handleAddAddress,
    handleSaveAddress,
    isSaving: saveAddressMutation.isPending,
    saveError: saveAddressMutation.error,
    addAddress,
    validateAddress,
    isSettingPrimary: setPrimaryAddressMutation.isPending,
  };
}
